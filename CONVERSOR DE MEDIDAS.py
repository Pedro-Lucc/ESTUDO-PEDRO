distancia = float(input('DIGITE A DISTÂNCIA (em metros): '))

km = distancia / 1000
hm = distancia / 100
dam = distancia * 10
dm = distancia * 100
cm = distancia * 1000
mm = distancia * 10000

print(f'A distância em km é {km}km')
print(f'A distância em hm é {hm}hm')
print(f'A distância em dam é {dam}dam')
print(f'A distância em dm é {dm}dm')
print(f'A distância em cm é {cm}cm')
print(f'A distância em mm é {mm}mm')


