#PROGRAMA QUE MOSTRA O DOBRO, TRIPLO E RAIZ DE UM NÚMERO DIGITADO

n = int(input('DIGITE UM NÚMERO: '))
dobro = n * 2
triplo = n * 3
raiz = n ** 0.5

print(f'O DROBRO DO NÚMERO SELECIONADO É {dobro}')
print(f'O TRIPPLO DO NÚMERO SELECIONADO É {triplo}')
print(f'A RAIZ QUADRADA DO NÚMERO SELECIONADO É {raiz}')
